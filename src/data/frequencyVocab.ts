export const getWordsByLevel = (level: 'A1' | 'A2'): FrequencyWord[] => {
  return frequencyVocabulary.filter(w => w.level === level);
};

// Get all verbs
export const getAllVerbs = (): FrequencyWord[] => {
  return frequencyVocabulary.filter(w => w.category === 'verbs');
};

// Search words
export const searchWords = (query: string): FrequencyWord[] => {
  const lowerQuery = query.toLowerCase();
  return frequencyVocabulary.filter(w => 
    w.french.toLowerCase().includes(lowerQuery) ||
    w.english.toLowerCase().includes(lowerQuery)
  );
};

// Get statistics
export const getVocabStats = () => {
  const total = frequencyVocabulary.length;
  const a1 = frequencyVocabulary.filter(w => w.level === 'A1').length;
  const a2 = frequencyVocabulary.filter(w => w.level === 'A2').length;
  const verbs = frequencyVocabulary.filter(w => w.category === 'verbs').length;
  const nouns = frequencyVocabulary.filter(w => 
    ['people', 'family', 'places', 'food', 'time', 'objects'].includes(w.category)
  ).length;
  const adjectives = frequencyVocabulary.filter(w => w.category === 'adjectives').length;
  
  return { total, a1, a2, verbs, nouns, adjectives };
};
